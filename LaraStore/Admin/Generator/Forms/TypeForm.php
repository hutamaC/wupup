use Form; 

class TypeForm{

    protected $attribute;
    /*
    |-------------------------------------------------------------------------------
    |
    | 构造函数
    |
    |-------------------------------------------------------------------------------
    */
    public function __construct(Array $attribute){

        $this->attribute        = $attribute;
        
    }



    
   

    /*
    |-------------------------------------------------------------------------------
    |
    | 返回生成表单数据
    |
    |-------------------------------------------------------------------------------
    */
    public function handle(){

        return  $this->make();
    }



    /*
    |-------------------------------------------------------------------------------
    |
    | 返回参数
    |
    |-------------------------------------------------------------------------------
    */
    public function make(){

    }


    /*
    |-------------------------------------------------------------------------------
    |
    | 获取参数
    |
    |-------------------------------------------------------------------------------
    */
    public function __get($key){

        return $this->attribute[$key];
    }

}